"""
The range () function :
  1. returns a sequence of numbers , starting from 0 by default 
  2. Increment by 1 (by default)
  3. ends at a specified number

"""
#loop upto 5 
for x in range (5) :
    print (x)

#loop up to 5 starting from 1
print()

for x in range(1,5):
    print(x)

# Step increase. For ex. print the table of 2.
print()
for x in range (2,21,2) :
    print(x)

#using else in for loop
print(x)
